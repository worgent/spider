package xiaobai.channel;

public class VideoDailyData {
	
	//视频名称
	private String videoName;
	//分成播放数
	private String videoPlayNum;
	//总收益
	private String videoTotalIncome;
	//详细地址
	private String videoDetailUrl;
	//视频id
	private String videoId;
	//日期
	private String dateTimes;
	//播放量
	private String palyNum;
	//pc播放量
	private String pcPlayNum;
	//phoneb播放量
	private String phonePlayNum;
	
	public String getVideoName() {
		return videoName;
	}
	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}
	public String getVideoPlayNum() {
		return videoPlayNum;
	}
	public void setVideoPlayNum(String videoPlayNum) {
		this.videoPlayNum = videoPlayNum;
	}
	public String getVideoTotalIncome() {
		return videoTotalIncome;
	}
	public void setVideoTotalIncome(String videoTotalIncome) {
		this.videoTotalIncome = videoTotalIncome;
	}
	public String getVideoDetailUrl() {
		return videoDetailUrl;
	}
	public void setVideoDetailUrl(String videoDetailUrl) {
		this.videoDetailUrl = videoDetailUrl;
	}
	public String getVideoId() {
		return videoId;
	}
	public void setVideoId(String videoId) {
		this.videoId = videoId;
	}
	public String getDateTimes() {
		return dateTimes;
	}
	public void setDateTimes(String dateTimes) {
		this.dateTimes = dateTimes;
	}
	public String getPalyNum() {
		return palyNum;
	}
	public void setPalyNum(String palyNum) {
		this.palyNum = palyNum;
	}
	public String getPcPlayNum() {
		return pcPlayNum;
	}
	public void setPcPlayNum(String pcPlayNum) {
		this.pcPlayNum = pcPlayNum;
	}
	public String getPhonePlayNum() {
		return phonePlayNum;
	}
	public void setPhonePlayNum(String phonePlayNum) {
		this.phonePlayNum = phonePlayNum;
	}
	
	
}
